const fs=require("fs");const {Client}=require("pg");
const env=fs.readFileSync(__dirname+"/../.env","utf8");
const url=env.match(/^DATABASE_URL="(.+)"/m)[1].replace(/[?&]schema=concierge/,"");
(async()=>{const c=new Client({connectionString:url});await c.connect();
const total=(await c.query(`select count(*)::int n from concierge."AnalyticsInquiry"`)).rows[0].n;
const cats=(await c.query(`select category,count(*)::int n from concierge."AnalyticsInquiry" group by 1 order by 2 desc`)).rows;
const sent=(await c.query(`select "endSentiment",count(*)::int n from concierge."AnalyticsInquiry" group by 1 order by 2 desc`)).rows;
const dsp=(await c.query(`select count(*)::int matched, percentile_cont(0.5) within group (order by "daysSincePurchase")::int med from concierge."AnalyticsInquiry" where "daysSincePurchase" is not null`)).rows[0];
const withEmail=(await c.query(`select count(*)::int n from concierge."AnalyticsInquiry" where "fromEmail" is not null`)).rows[0].n;
console.log("TOTAL inquiries (365d):",total,"| with sender email:",withEmail);
console.log("CATEGORIES:",cats.map(r=>`${r.category}=${r.n}`).join(", "));
console.log("SENTIMENT:",sent.map(r=>`${r.endSentiment}=${r.n}`).join(", "));
console.log("PURCHASE-MATCHED:",dsp.matched,"| median days since purchase:",dsp.med);
await c.end();})().catch(e=>{console.error(e.message);process.exit(1);});
