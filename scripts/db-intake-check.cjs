const fs=require("fs");const {Client}=require("pg");
const env=fs.readFileSync(__dirname+"/../.env","utf8");
const url=env.match(/^DATABASE_URL="(.+)"/m)[1].replace(/[?&]schema=concierge/,"");
(async()=>{const c=new Client({connectionString:url});await c.connect();
const mock=(await c.query(`select count(*)::int n from concierge."Ticket" where "providerThreadId" like 'mock%'`)).rows[0].n;
const real=(await c.query(`select count(*)::int n from concierge."Ticket" where "providerThreadId" not like 'mock%'`)).rows[0].n;
const sample=(await c.query(`select t.subject, cu."displayName", cu.email from concierge."Ticket" t join concierge."Customer" cu on cu.id=t."customerId" where t."providerThreadId" not like 'mock%' order by t."createdAt" desc limit 6`)).rows;
console.log("mock tickets:",mock,"| real gmail tickets:",real);
console.log("\nreal inquiries pulled from hello@:");
sample.forEach(r=>{const dom=(r.email||'').split('@')[1]||''; console.log(`  • ${r.displayName||'(no name)'} @${dom} — "${(r.subject||'(no subject)').slice(0,60)}"`);});
await c.end();})().catch(e=>{console.error(e.message);process.exit(1);});
