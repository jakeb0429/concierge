const fs=require("fs");const {Client}=require("pg");
const env=fs.readFileSync(__dirname+"/../.env","utf8");
const url=env.match(/^DATABASE_URL="(.+)"/m)[1].replace(/[?&]schema=concierge/,"");
(async()=>{const c=new Client({connectionString:url});await c.connect();
const cols=(await c.query(`select column_name,data_type from information_schema.columns where table_schema='public' and table_name='Order' order by ordinal_position limit 20`)).rows;
console.log("Order columns:",cols.map(r=>r.column_name+":"+r.data_type.slice(0,9)).join(", "));
const stats=(await c.query(`select count(*)::int n, min("createdAt")::date mn, max("createdAt")::date mx from public."Order"`)).rows[0];
console.log("Order rows:",stats.n,"| range:",stats.mn,"→",stats.mx);
const yr=(await c.query(`select count(*)::int n, count(distinct email)::int emails, sum("totalPrice")::numeric(12,0) rev from public."Order" where "createdAt" > now() - interval '365 days'`)).rows[0];
console.log("last 365d:",yr.n,"orders,",yr.emails,"unique emails, $"+yr.rev);
await c.end();})().catch(e=>{console.error(e.message);process.exit(1);});
