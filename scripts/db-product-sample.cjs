const fs=require("fs");const {Client}=require("pg");
const env=fs.readFileSync(__dirname+"/../.env","utf8");
const url=env.match(/^DATABASE_URL="(.+)"/m)[1].replace(/[?&]schema=concierge/,"");
(async()=>{const c=new Client({connectionString:url});await c.connect();
const fresh=(await c.query(`select max("lastSyncedAt") mx, min("lastSyncedAt") mn from public."Product"`)).rows[0];
console.log("Product lastSyncedAt range:",fresh.mn,"→",fresh.mx);
const sample=(await c.query(`select sku,name,price,quantity,replenishment from public."Product" order by name limit 8`)).rows;
sample.forEach(r=>console.log(` ${r.sku} | ${r.name} | $${r.price} | qty ${r.quantity} | replen: ${r.replenishment}`));
const stats=(await c.query(`select count(*)::int total, count(*) filter (where quantity<=0)::int oos, count(*) filter (where replenishment is not null and replenishment!='')::int replen from public."Product"`)).rows[0];
console.log("stats:",JSON.stringify(stats));
const fba=(await c.query(`select "reportPulledAt" from public."FbaRestockRecommendation" limit 1`)).rows[0];
console.log("FBA report pulled:",fba?.reportPulledAt);
await c.end();})().catch(e=>{console.error(e.message);process.exit(1);});
